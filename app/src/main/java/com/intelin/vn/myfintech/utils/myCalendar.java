public class myCalendar {

    private int day;

    private int month;

    private int year;

    public int getDay() {
        return day;
    }

    public myCalendar setDay(int day) {
        this.day = day;
        return this;
    }

    public int getMonth() {
        return month;
    }

    public myCalendar setMonth(int month) {
        this.month = month;
        return this;
    }

    public int getYear() {
        return year;
    }

    public myCalendar setYear(int year) {
        this.year = year;
        return this;
    }
}
